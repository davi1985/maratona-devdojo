package academy.devdojo.maratonajava.intro;

public class Lesson04Operators {
    public static void main(String[] args) {
        // Math operators: + - / *
        int numberOne = 10;
        int numberTwo = 20;

        int result = numberTwo - numberOne;
        System.out.println(result);
        result = numberTwo + numberOne;
        System.out.println(result);
        result = numberTwo / numberOne;
        System.out.println(result);
        result = numberTwo * numberOne;
        System.out.println(result);

    }
}
